#include <stdio.h>
//construa uma tabuada do 1 ao 10 e faca somatoria dos resultados de cada uma;
    int main() {

        int coleta[10];
        int i;
        for(i = 1; i <= 10; i++) {
           coleta[1] = i * 1; 
           coleta[2] = i * 2;
           coleta[3] = i * 3;
           coleta[4] = i * 4;
           coleta[5] = i * 5;
           coleta[6] = i * 6;
           coleta[7] = i * 7;
           coleta[8] = i * 9;
           coleta[9] = i * 10;

        while(i == 10) {
            i * 1;
            i * 2;
            i * 3;
            i * 4;
            i * 5;
            i * 6;
            i * 7;
            i * 8; 
            i * 9;
            i * 10;
        }
        }
        return 0;
}

